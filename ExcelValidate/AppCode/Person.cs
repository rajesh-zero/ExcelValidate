class Person
{

    private string name, dateOfBirth, isActive, balance, loanAmount;

    public string Name { get => name; set => name = value; }
    public string DateOfBirth { get => dateOfBirth; set => dateOfBirth = value; }
    public string IsActive { get => isActive; set => isActive = value; }
    public string Balance { get => balance; set => balance = value; }
    public string LoanAmount { get => loanAmount; set => loanAmount = value; }

}

